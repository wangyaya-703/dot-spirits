export function createWhitePng(width, height) {
  const png = new PNG({ width, height });
  png.data.fill(255);
  return png;
}

export function setPixel(png, x, y, value = 0) {
  if (x < 0 || y < 0 || x >= png.width || y >= png.height) {
    return;
  }

  const idx = (png.width * y + x) << 2;
  png.data[idx] = value;
  png.data[idx + 1] = value;
  png.data[idx + 2] = value;
  png.data[idx + 3] = 255;
}

export function drawRect(png, x, y, width, height, fill = 0) {
  for (let py = y; py < y + height; py += 1) {
    for (let px = x; px < x + width; px += 1) {
      setPixel(png, px, py, fill);
    }
  }
}

export function measureBitmapText(text, scale = 1) {
  const normalized = String(text || '').toUpperCase();
  if (!normalized) {
    return 0;
  }

  let width = 0;
  for (const char of normalized) {
    const glyph = FONT_5X7[char] || FONT_5X7[' '];
    width += (glyph[0].length + 1) * scale;
  }

  return width - scale;
}

export function drawBitmapText(png, text, x, y, scale = 1) {
  const normalized = String(text || '').toUpperCase();
  let cursorX = x;

  for (const char of normalized) {
    const glyph = FONT_5X7[char] || FONT_5X7[' '];
    for (let row = 0; row < glyph.length; row += 1) {
      for (let col = 0; col < glyph[row].length; col += 1) {
        if (glyph[row][col] === '1') {
          drawRect(png, cursorX + col * scale, y + row * scale, scale, scale, 0);
        }
      }
    }

    cursorX += (glyph[0].length + 1) * scale;
  }
}
